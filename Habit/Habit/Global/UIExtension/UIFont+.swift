import Foundation
import UIKit

// MARK: - UIFont Extension

extension UIFont {
    
    class func SFTextRegular(fontSize: CGFloat) ->  UIFont {
        return UIFont(name: "SFProText-Regular", size: fontSize)!
    }
    
    class func SFTextBold(fontSize: CGFloat) ->  UIFont {
        return UIFont(name: "SFProText-Bold", size: fontSize)!
    }
    
    class func SFTextSemibold(fontSize: CGFloat) ->  UIFont {
        return UIFont(name: "SFProText-Semibold", size: fontSize)!
    }
    
    class func SFTextMedium(fontSize: CGFloat) ->  UIFont {
        return UIFont(name: "SFProText-Medium", size: fontSize)!
    }
    
    class func SFTextLight(fontSize: CGFloat) ->  UIFont {
        return UIFont(name: "SFProText-Light", size: fontSize)!
    }
    
    class func SFDisplayRegular(fontSize: CGFloat) ->  UIFont {
        return UIFont(name: "SFProDisplay-Regular", size: fontSize)!
    }
    
    class func SFDisplayBold(fontSize: CGFloat) ->  UIFont {
        return UIFont(name: "SFProDisplay-Bold", size: fontSize)!
    }
    
    class func SFDisplaySemibold(fontSize: CGFloat) ->  UIFont {
        return UIFont(name: "SFProDisplay-Semibold", size: fontSize)!
    }
    
    class func SFDisplayMedium(fontSize: CGFloat) ->  UIFont {
        return UIFont(name: "SFProDisplay-Medium", size: fontSize)!
    }
    
    class func SFDisplayLight(fontSize: CGFloat) ->  UIFont {
        return UIFont(name: "SFProDisplay-Light", size: fontSize)!
    }
}
